public class Test1 {

    public static void main(String[] args) {
        int a = 5;
        int b = 5;

        System.out.println(++a);  // Outputs 6
        System.out.println(a);    // Outputs 6

        System.out.println(b++);  // Outputs 5
        System.out.println(b);    // Outputs 6
    }
}
